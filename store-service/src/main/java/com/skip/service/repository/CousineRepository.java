package com.skip.service.repository;

import java.util.List;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

import com.skip.service.domain.Cousine;

@Repository
public interface CousineRepository extends JpaRepository<Cousine, Long> {

	@Query("select u from #{#entityName} u where u.name like %?1%")
	List<Cousine> findByName(@Param("name") String name);

}
